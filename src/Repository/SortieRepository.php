    public function findSortiesPasInscrit(){
        return $this->createQueryBuilder('s')
            ->andWhere(":user IN(s.participants)")
            ->setParameter('user', get_current_user())
            ->orderBy('s.nom', 'ASC')
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }

